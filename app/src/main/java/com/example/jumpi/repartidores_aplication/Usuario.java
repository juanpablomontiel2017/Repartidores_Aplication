package com.example.jumpi.repartidores_aplication;

import android.content.Context;
import android.content.SharedPreferences;

import static android.content.Context.MODE_PRIVATE;

public class Usuario {


    private String IdPersona;

    private String DNI;

    private String Usuario;

    private String Password;

    private String Tipo_de_Usuario;




    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    Usuario(){

    }


    Usuario(String IdPersona, String DNI, String Usuario, String Password,String Tipo_de_Usuario){

        this.setIdPersona(IdPersona);

        this.setDNI(DNI);

        this.setUsuario(Usuario);

        this.setPassword(Password);

        this.setTipo_de_Usuario(Tipo_de_Usuario);

    }/************** FIN DEL CONSTRUCTOR Usuario *****************/




    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/


    public void GuardarUsuarioEnUnSharedPreferences(Context context){

        SharedPreferences sharedPreferences = context.getSharedPreferences("Datos_Usuario", MODE_PRIVATE);
        SharedPreferences.Editor editor = sharedPreferences.edit();


        editor.putString("IdPersona",this.getIdPersona());
        editor.commit();


        editor.putString("DNI",this.getDNI());
        editor.commit();


        editor.putString("Usuario",this.getUsuario());
        editor.commit();


        editor.putString("Password",this.getPassword());
        editor.commit();


        editor.putString("Tipo_de_Usuario", this.getTipo_de_Usuario());
        editor.commit();


    }/********************* FIN DE LA FUNCIÓN GuardarUsuarioEnUnSharedPreferences()***********************/


    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    public void LeerUsuarioEnUnSharedPreferences(Context context){

        SharedPreferences preferences = context.getSharedPreferences("Datos_Usuario", MODE_PRIVATE);

        this.setIdPersona(preferences.getString("IdPersona",""));

        this.setDNI(preferences.getString("DNI",""));

        this.setUsuario(preferences.getString("Usuario",""));

        this.setPassword(preferences.getString("Password",""));

        this.setTipo_de_Usuario(preferences.getString("Tipo_de_Usuario",""));





    }/********************* FIN DE LA FUNCIÓN LeerUsuarioEnUnSharedPreferences()***********************/



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    public String getIdPersona() {

        return IdPersona;

    }




    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/




    public void setIdPersona(String idPersona) {

        IdPersona = idPersona;

    }




    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/

    public String getDNI() {

        return DNI;
    }



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/

    public void setDNI(String DNI) {

        this.DNI = DNI;
    }

    /**********************************************************************************************/
    /**********************************************************************************************/

    public String getUsuario() {

        return Usuario;

    }



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    public void setUsuario(String usuario) {

        Usuario = usuario;

    }



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    public String getPassword() {

        return Password;

    }



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/




    public void setPassword(String password) {

        Password = password;

    }



    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/
    /**********************************************************************************************/



    public String getTipo_de_Usuario() {
        return Tipo_de_Usuario;
    }



    public void setTipo_de_Usuario(String tipo_de_Usuario) {
        Tipo_de_Usuario = tipo_de_Usuario;
    }





}/******************************** FIN DE LA CLASE Usuario ******************************************/
